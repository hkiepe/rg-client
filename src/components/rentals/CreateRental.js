class CreateRental extends Component {
  state = {
    open: false,
    email: '',
    telephone: '',
    pesel: '',
    firstName: '',
    lastName: '',
    advancePayment: '',
    vehicles: [],
    errors: {}
  };

  UNSAFE_componentWillReceiveProps(nextProps) {
    if (nextProps.UI.errors) {
      this.setState({
        errors: nextProps.UI.errors
      });
    }
    if (!nextProps.UI.errors && !nextProps.UI.loading) {
      this.setState({
        open: false,
        email: '',
        telephone: '',
        pesel: '',
        firstName: '',
        lastName: '',
        advancePayment: '',
        vehicles: [],
        errors: {}
      });
    }
  }
  handleOpen = () => {
    this.setState({ open: true });
  };
  handleClose = () => {
    this.props.clearErrors();
    this.setState({ open: false, errors: {} });
  };
  handleChange = event => {
    this.setState({ [event.target.name]: event.target.value });
  };
  handleSubmit = event => {
    console.log('handle Submit');
    console.log(this.state);
    event.preventDefault();
    this.props.postRental({
      email: this.state.email,
      telephone: this.state.telephone,
      pesel: this.state.pesel,
      firstName: this.state.pesel,
      lastName: this.state.lastName,
      advancePayment: this.state.advancePayment,
      vehicles: this.state.vehicles
    });
  };
  render() {
    const { errors } = this.state;
    const {
      classes,
      UI: { loading }
    } = this.props;
    return (
      <Fragment>
        <MyButton onClick={this.handleOpen} tip="Create rental">
          <AddIcon />
        </MyButton>
        <Dialog
          open={this.state.open}
          onClose={this.handleClose}
          fullScreen
          maxWidth="sm"
        >
          <MyButton
            tip="Close"
            onClick={this.handleClose}
            tipClassName={classes.closeButton}
          >
            <CloseIcon />
          </MyButton>
          <DialogTitle>Post a new rental transaction</DialogTitle>
          <DialogContent>
            <form onSubmit={this.handleSubmit}>
              <TextField
                name="email"
                type="text"
                label="Email"
                multiline
                rows="3"
                placeholder="Fill in the mail adress"
                error={errors.body ? true : false}
                helperText={errors.body}
                className={classes.textField}
                onChange={this.handleChange}
                fullWidth
              />
              <TextField
                name="telephone"
                type="text"
                label="Telephone"
                multiline
                rows="3"
                placeholder="Fill in the telephone number"
                error={errors.body ? true : false}
                helperText={errors.body}
                className={classes.textField}
                onChange={this.handleChange}
                fullWidth
              />
              <TextField
                name="pesel"
                type="text"
                label="Pesel"
                multiline
                rows="3"
                placeholder="Fill in the Pesel"
                error={errors.body ? true : false}
                helperText={errors.body}
                className={classes.textField}
                onChange={this.handleChange}
                fullWidth
              />
              <TextField
                name="firstName"
                type="text"
                label="First name"
                multiline
                rows="3"
                placeholder="Fill in the first name"
                error={errors.body ? true : false}
                helperText={errors.body}
                className={classes.textField}
                onChange={this.handleChange}
                fullWidth
              />
              <TextField
                name="lastName"
                type="text"
                label="Last name"
                multiline
                rows="3"
                placeholder="Fill in the last name"
                error={errors.body ? true : false}
                helperText={errors.body}
                className={classes.textField}
                onChange={this.handleChange}
                fullWidth
              />
              <TextField
                name="advancePayment"
                type="text"
                label="Advance payment"
                multiline
                rows="3"
                placeholder="Fill in the advance payment"
                error={errors.body ? true : false}
                helperText={errors.body}
                className={classes.textField}
                onChange={this.handleChange}
                fullWidth
              />
              <TextField
                name="vehicles"
                type="text"
                label="Vehicles"
                multiline
                rows="3"
                placeholder="Fill in the vehicles"
                error={errors.body ? true : false}
                helperText={errors.body}
                className={classes.textField}
                onChange={this.handleChange}
                fullWidth
              />
              <Button
                type="submit"
                variant="contained"
                color="primary"
                className={classes.submitButton}
                disabled={loading}
              >
                Submit
                {loading && (
                  <CircularProgress
                    size={30}
                    className={classes.progressSpinner}
                  />
                )}
              </Button>
            </form>
          </DialogContent>
        </Dialog>
      </Fragment>
    );
  }
}

CreateRental.propTypes = {
  postRental: PropTypes.func.isRequired,
  clearErrors: PropTypes.func.isRequired,
  UI: PropTypes.object.isRequired
};

const mapStateToProps = state => ({
  UI: state.UI
});

export default connect(mapStateToProps, { postRental, clearErrors })(
  withStyles(styles)(CreateRental)
);
